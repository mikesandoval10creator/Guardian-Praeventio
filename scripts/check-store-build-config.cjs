#!/usr/bin/env node
/**
 * Praeventio Guard — store-build guard: the DEV SERVER must never ship.
 *
 * `capacitor.config.ts` only injects the live-reload block when NODE_ENV is
 * not "production":
 *
 *   ...(!isProd && { server: { url: 'http://10.0.2.2:5173', cleartext: true } })
 *
 * `npx cap sync` BAKES the resolved config into
 * `android/app/src/main/assets/capacitor.config.json` — and THAT file is what
 * actually ships inside the APK/AAB. So a sync run without
 * `NODE_ENV=production` embeds the Android-emulator host alias (10.0.2.2) plus
 * cleartext HTTP into a store build: on a real device the WebView tries to load
 * an unreachable dev server and the app opens **blank**, with cleartext traffic
 * enabled on top.
 *
 * The Android build is produced LOCALLY (ADR-0006 — there is no Gradle job in
 * CI), so CI alone cannot catch this. Run this guard after `cap sync` and
 * before packaging a release:
 *
 *   node scripts/check-store-build-config.cjs      # or: npm run guard:store-build
 *
 * Exit 0 when the synced config is store-safe (or when nothing has been synced
 * yet). Exit 1, naming the offending keys, otherwise.
 *
 * The companion CI gate lives in `src/__tests__/mobile/storeBuildConfig.test.ts`:
 * it pins the `capacitor.config.ts` LOGIC (prod => no server block) so the
 * `!isProd` guard cannot be dropped unnoticed. This script pins the ARTIFACT.
 *
 * Modeled after scripts/precommit-allowbackup-guard.cjs.
 */

const fs = require('node:fs');
const path = require('node:path');

const ASSET_RELATIVE = path.join(
  'android',
  'app',
  'src',
  'main',
  'assets',
  'capacitor.config.json',
);

/**
 * Pure check — exported so the vitest gate can exercise it on fixtures without
 * touching the filesystem.
 *
 * @param {unknown} cfg parsed capacitor.config.json
 * @returns {string[]} human-readable problems; empty means store-safe
 */
function findStoreBuildProblems(cfg) {
  const problems = [];
  if (!cfg || typeof cfg !== 'object') return problems;

  const server = cfg.server;
  if (server && typeof server === 'object') {
    if (server.url) problems.push(`server.url = ${JSON.stringify(server.url)}`);
    if (server.cleartext === true) problems.push('server.cleartext = true');
  }

  const android = cfg.android;
  if (android && typeof android === 'object') {
    if (android.webContentsDebuggingEnabled === true) {
      problems.push('android.webContentsDebuggingEnabled = true');
    }
    if (android.allowMixedContent === true) {
      problems.push('android.allowMixedContent = true');
    }
  }

  return problems;
}

function main() {
  const assetPath = path.resolve(process.cwd(), ASSET_RELATIVE);

  if (!fs.existsSync(assetPath)) {
    console.log(
      `[store-build-guard] SKIP — ${ASSET_RELATIVE} not present (nothing synced yet).`,
    );
    return 0;
  }

  let cfg;
  try {
    cfg = JSON.parse(fs.readFileSync(assetPath, 'utf8'));
  } catch (err) {
    console.error(
      `[store-build-guard] FAIL — cannot parse ${ASSET_RELATIVE}: ${err.message}`,
    );
    return 1;
  }

  const problems = findStoreBuildProblems(cfg);

  if (problems.length === 0) {
    console.log(
      '[store-build-guard] PASS — synced config is store-safe (no dev server, no cleartext, no debugging).',
    );
    return 0;
  }

  console.error(
    '[store-build-guard] FAIL — the DEV config was baked into the store build:',
  );
  for (const p of problems) console.error(`  • ${p}`);
  console.error('');
  console.error(
    `  ${ASSET_RELATIVE} is generated by \`npx cap sync\`. Re-sync in production mode:`,
  );
  console.error(
    '    NODE_ENV=production npm run build && NODE_ENV=production npx cap sync android',
  );
  console.error('');
  console.error(
    '  Shipping this points the WebView at the emulator host (10.0.2.2) — the app',
  );
  console.error(
    '  opens BLANK on a real device — and enables cleartext HTTP traffic.',
  );
  return 1;
}

module.exports = { findStoreBuildProblems, ASSET_RELATIVE };

if (require.main === module) {
  process.exit(main());
}
